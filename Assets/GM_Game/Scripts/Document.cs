    *在Unity中目前有两种输入系统，一种是旧输入系统，一种是新输入系统。
    *1、旧输入系统（Legacy Input System）:基于Input类，使用静态接口访问输入数据
        - 优点：兼容性好，适用于旧版本Unity
        - 缺点：功能优先，拓展性差，不支持复杂输入系统和多设备管理
    *2、新输入系统（Input System）：基于模块化、事件驱动。支持更多设备和复杂输入映射
        - 优点：功能强大，支持复杂输入系统和多设备管理
        - 缺点：学习难度大，配置较为复杂
